// 翻译辅助函数
export function getTranslation(language: "zh" | "en", key: string, params?: Record<string, string | number>): string {
  const translationsForLanguage = translations[language] as Record<string, any>;
  let translation = translationsForLanguage[key] || key;
  
  // 如果有参数，替换参数
  if (params && typeof translation === 'string') {
    Object.keys(params).forEach(param => {
      translation = translation.replace(
        new RegExp(`{${param}}`, 'g'),
        String(params[param])
      );
    });
  }
  
  return translation;
} 